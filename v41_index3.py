# ── SL(2,Z) matrix arithmetic (exact, using Python ints) ──

def mat2(a, b, c, d):
    """Create a 2x2 integer matrix as tuple of tuples."""
    return ((a, b), (c, d))

def mat_mul(A, B):
    """Multiply two 2x2 matrices."""
    return ((A[0][0]*B[0][0] + A[0][1]*B[1][0], A[0][0]*B[0][1] + A[0][1]*B[1][1]),
            (A[1][0]*B[0][0] + A[1][1]*B[1][0], A[1][0]*B[0][1] + A[1][1]*B[1][1]))

def mat_mul_mod(A, B, p):
    """Multiply two 2x2 matrices mod p."""
    return (((A[0][0]*B[0][0] + A[0][1]*B[1][0]) % p, (A[0][0]*B[0][1] + A[0][1]*B[1][1]) % p),
            ((A[1][0]*B[0][0] + A[1][1]*B[1][0]) % p, (A[1][0]*B[0][1] + A[1][1]*B[1][1]) % p))

def mat_det(A):
    return A[0][0]*A[1][1] - A[0][1]*A[1][0]

def mat_inv(A):
    """Inverse of 2x2 matrix in SL(2,Z) (det=1)."""
    d = mat_det(A)
    assert d == 1 or d == -1, f"det={d}"
    return ((d*A[1][1], -d*A[0][1]), (-d*A[1][0], d*A[0][0]))

def mat_mod(A, p):
    return ((A[0][0] % p, A[0][1] % p), (A[1][0] % p, A[1][1] % p))

def mat_eq(A, B):
    return A[0][0]==B[0][0] and A[0][1]==B[0][1] and A[1][0]==B[1][0] and A[1][1]==B[1][1]

def mat_str(A):
    return f"[[{A[0][0]},{A[0][1]}],[{A[1][0]},{A[1][1]}]]"

