# ── Theta group Gamma_theta ──
# Gamma_theta = { [[a,b],[c,d]] in SL(2,Z) : a*d + b*c = 0 mod 2 }
# Equivalently: a+d even (i.e., trace even), or a+d odd with b,c even.
# More precisely: Gamma_theta is generated by T^2 and S.
# Actually, Gamma_theta = { M in SL(2,Z) : M = [[a,b],[c,d]], ab+cd = 0 mod 2 }
# i.e., either both a,c have same parity, or both b,d have same parity.
# Standard definition: Gamma_theta = <S, T^2> (index 3 in SL(2,Z)).

def in_gamma_theta(M):
    """Check if M is in Gamma_theta.
    Gamma_theta consists of matrices [[a,b],[c,d]] in SL(2,Z) where
    ab ≡ 0 (mod 2) and cd ≡ 0 (mod 2).
    Equivalently: a≡d (mod 2) and b≡c (mod 2).
    """
    a, b, c, d = M[0][0], M[0][1], M[1][0], M[1][1]
    # Standard: Gamma_theta = { M : a*b + c*d ≡ 0 mod 2 }
    # Actually the precise condition for Gamma_theta:
    # M ≡ [[1,0],[0,1]] or [[0,1],[1,0]] mod 2
    # i.e., M mod 2 is in {I, S} where S=[[0,-1],[1,0]]≡[[0,1],[1,0]] mod 2
    a2, b2, c2, d2 = a%2, b%2, c%2, d%2
    if (a2, b2, c2, d2) == (1, 0, 0, 1):
        return True  # ≡ I mod 2
    if (a2, b2, c2, d2) == (0, 1, 1, 0):
        return True  # ≡ S mod 2
    return False

def coset_index(M):
    """Return coset index 0,1,2 for M in SL(2,Z)/Gamma_theta.
    Coset 0: Gamma_theta (M ≡ I or S mod 2)
    Coset 1: T * Gamma_theta (M ≡ T or TS mod 2)
    Coset 2: T^{-1} * Gamma_theta (M ≡ T^{-1} or T^{-1}S mod 2)
    """
