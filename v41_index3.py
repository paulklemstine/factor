    # SL(2,Z/2Z) = S_3 has 6 elements. Gamma_theta mod 2 has 2 elements: I, S.
    # The 3 cosets mod 2:
    # Coset 0: {I=[[1,0],[0,1]], S=[[0,1],[1,0]]}
