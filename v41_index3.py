def exp2_modular_curve_cover():
    emit("\n--- Modular curve analysis ---")
    emit("Gamma_theta = theta group, also written Gamma_theta or Gamma^theta.")
    emit("X_theta = Gamma_theta \\ H* is the modular curve for Gamma_theta.")

    # Genus computation via Riemann-Hurwitz
    # SL(2,Z) \\ H* has genus 0 (this is X(1), the j-line).
    # For a subgroup Gamma of index n in SL(2,Z):
    # genus(Gamma\\H*) = 1 + n/12 - e2/4 - e3/3 - c/2
    # where e2 = # elliptic pts of order 2, e3 = order 3, c = cusps.

    emit("\nGenus formula: g = 1 + mu/12 - e2/4 - e3/3 - c_inf/2")
    emit("where mu = index, e2 = elliptic pts order 2, e3 = order 3, c_inf = cusps.\n")

    # For Gamma_theta, index mu = 3:
    # Cusps: Gamma_theta has 2 cusps (0 and infinity).
    # Elliptic points: Need to check if S (order 4 in PSL, maps to order 2 ell pt)
    # and ST (order 6 in PSL, maps to order 3 ell pt) are in Gamma_theta.
    # S is in Gamma_theta (coset 0). So the order-2 elliptic point i is NOT split.
    # Actually: e2 counts fixed points of elements of order 2 in PSL.
    # If an element of order 2 in SL(2,Z) (like S, which satisfies S^2=-I≡I in PSL) is in Gamma,
    # then it fixes a point, contributing to e2.

    # S ∈ Gamma_theta → the elliptic point tau=i is fixed by Gamma_theta.
    # In PSL(2,Z), S has order 2 and fixes tau=i. Since S ∈ Gamma_theta,
    # e2 for Gamma_theta: we need #{Gamma_theta-orbits of elliptic pts of order 2}

    # For index-mu subgroup: e2 = #{coset reps g : g^{-1}Sg ∈ Gamma or equiv, fixed by S on cosets}
    # S permutes the 3 cosets. S fixes coset 0 (since S ∈ Gamma_theta).
    # S on coset 1: S*(T*Gamma_theta) = ST*Gamma_theta = coset 2.
    # S on coset 2: S*(ST*Gamma_theta) = S^2*T*Gamma_theta = -T*Gamma_theta = T*Gamma_theta = coset 1.
    # So S: 0->0, 1->2, 2->1. Fixed points of S on cosets: just {0}. So e2 = 1.

    # For order 3: ST has order 3 in PSL(2,Z) (actually order 6 in SL, order 3 in PSL).
    # ST ∈ coset 2, not in Gamma_theta. So it doesn't fix coset 0.
    # Action of ST on cosets:
    # ST*coset 0 = coset 2 (ST ∈ coset 2).
    # ST*coset 1: ST*T*Gamma_theta = ST^2*Gamma_theta.
    #   ST^2 = [[0,-1],[1,0]]*[[1,2],[0,1]] = [[0,-1],[1,2]].
    #   coset_index of [[0,-1],[1,2]]: mod 2 = [[0,1],[1,0]] = coset 0. So ST*coset 1 = coset 0.
    # ST*coset 2: ST*ST*Gamma_theta = (ST)^2*Gamma_theta.
    #   (ST)^2 = [[0,-1],[1,1]]^2 = [[-1,-1],[1,0]]. mod 2 = [[1,1],[1,0]] → coset 1. So ST*coset 2 = coset 1.
    # ST: 0->2, 1->0, 2->1. This is a 3-cycle. No fixed points. So e3 = 0.

    e2 = 1
    e3 = 0

    # Cusps of Gamma_theta: The cusps of SL(2,Z) are just {infinity}.
    # For Gamma_theta of index 3, the cusps are orbits of Q∪{∞} under Gamma_theta.
    # T acts on cusps: T(∞)=∞, T(0)=1.
    # Gamma_theta ∋ T^2: T^2(∞)=∞.
    # Gamma_theta ∋ S: S(∞)=0, S(0)=∞. So 0 and ∞ are in the same orbit?
    # Yes! S ∈ Gamma_theta and S(∞)=0, so ∞ and 0 are the same cusp.
    # What about cusp 1? T(∞)=∞... not helpful. Is there a cusp at 1?
    # Cusp width: parabolic elements fixing cusps.
    # The number of cusps = number of Gamma_theta-orbits on P^1(Q).
