def cf_expansion(a, b, max_terms=20):
    """Continued fraction expansion of a/b."""
    terms = []
    for _ in range(max_terms):
        if b == 0:
            break
        q, r = divmod(a, b)
        terms.append(q)
        a, b = b, r
    return terms

