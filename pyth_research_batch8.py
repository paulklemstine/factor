print("\n--- Experiment 1: Is there a Pythagorean → Markov morphism? ---")

def markov_mutation(x, y, z):
    """Markov mutation: replace z with 3xy - z."""
    return (x, y, 3*x*y - z)

# Generate Markov tree
markov_root = (1, 1, 1)
print(f"  Markov root: {markov_root}")

def markov_tree(node, depth):
    if depth == 0:
        return [node]
    x, y, z = node
    children = [
        markov_mutation(x, y, z),   # mutate z
        markov_mutation(x, z, y),   # mutate y (permute then mutate)
        markov_mutation(y, z, x),   # mutate x
    ]
    result = [node]
    for child in children:
        result.extend(markov_tree(child, depth - 1))
    return result

markov_nodes = markov_tree((1, 1, 1), 4)
markov_values = set()
for x, y, z in markov_nodes:
    markov_values.update([abs(x), abs(y), abs(z)])

