def berggren_mod_p(p):
    """Compute the group generated by Berggren matrices mod p."""
    mats = set()
    B1m = tuple(map(tuple, (B1 % p).tolist()))
    B2m = tuple(map(tuple, (B2 % p).tolist()))
    B3m = tuple(map(tuple, (B3 % p).tolist()))

    def mat_mul_mod(A, B, p):
        res = [[0]*3 for _ in range(3)]
        for i in range(3):
            for j in range(3):
                s = 0
                for k in range(3):
                    s += A[i][k] * B[k][j]
                res[i][j] = s % p
        return tuple(tuple(r) for r in res)

    def mat_inv(A_tup):
        """Try all elements to find inverse (brute force for small groups)."""
        I = tuple(tuple(1 if i==j else 0 for j in range(3)) for i in range(3))
        for B_tup in mats:
            if mat_mul_mod(A_tup, B_tup, p) == I:
                return B_tup
        return None

    gens = [B1m, B2m, B3m]
    # Also add inverses
    # Compute inverses of Berggren matrices mod p
    # B1^{-1} mod p: use adjugate
    def mat_inv_direct(M, p):
        a = [[M[i][j] for j in range(3)] for i in range(3)]
        # Cofactor matrix
        cof = [[0]*3 for _ in range(3)]
        for i in range(3):
            for j in range(3):
                minor = []
                for ii in range(3):
                    if ii == i: continue
                    row = []
                    for jj in range(3):
                        if jj == j: continue
                        row.append(a[ii][jj])
                    minor.append(row)
                cof[i][j] = ((-1)**(i+j)) * (minor[0][0]*minor[1][1] - minor[0][1]*minor[1][0])
        # det
        det = sum(a[0][j]*cof[0][j] for j in range(3))
        det_mod = det % p
        if det_mod == 0:
            return None
        # modular inverse of det
        det_inv = pow(det_mod, p-2, p)
        # adjugate = transpose of cofactor
        inv = [[0]*3 for _ in range(3)]
        for i in range(3):
            for j in range(3):
                inv[i][j] = (cof[j][i] * det_inv) % p
        return tuple(tuple(r) for r in inv)

    for g in [B1m, B2m, B3m]:
        gi = mat_inv_direct(g, p)
        if gi:
            gens.append(gi)

    queue = list(gens)
    for g in gens:
        mats.add(g)

    while queue:
        if len(mats) > 50000:
            break
        a = queue.pop(0)
        for g in gens:
            for prod in [mat_mul_mod(a, g, p), mat_mul_mod(g, a, p)]:
                if prod not in mats:
                    mats.add(prod)
                    queue.append(prod)

    return mats

def chebyshev_T(n, x):
    """Chebyshev polynomial T_n(x) via recurrence."""
    if n == 0: return 1.0
    if n == 1: return x
    t0, t1 = 1.0, x
    for _ in range(2, n+1):
        t0, t1 = t1, 2*x*t1 - t0
    return t1

def chebyshev_T_deriv(n, x):
    """Derivative T_n'(x) = n * U_{n-1}(x)."""
    if n == 0: return 0.0
    if n == 1: return 1.0
    # U_{n-1}(x) via recurrence
    u0, u1 = 1.0, 2*x
    for _ in range(2, n):
        u0, u1 = u1, 2*x*u1 - u0
    return n * u1

